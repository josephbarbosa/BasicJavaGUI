    private void SetAddressButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_SetAddressButtonActionPerformed
        String address = AddressBox.getText();
        String city = CityBox.getText();
        String state = StateBox.getText();
        
        if (address.equalsIgnoreCase("") || city.equalsIgnoreCase("") || state.equalsIgnoreCase(""))
        {
            if (address.equalsIgnoreCase(""))
            {
                jLabel3.setText("Please enter an address.");
                address = AddressBox.getText();
            }
            if (city.equalsIgnoreCase(""))
            {
                jLabel3.setText("Please enter a city.");
                city = CityBox.getText();
            }
            if (state.equalsIgnoreCase(""))
            {
                jLabel3.setText("Please enter a state.");
                state = StateBox.getText();
            }      
        }
        else
        {
            AddressBox.setText(""); 
            CityBox.setText("");
            StateBox.setText("");
            object.setUserAddress(address);
            object.setUserCity(city);
            object.setUserState(state);
            jProgressBar1.setValue(40);
            AddressBox.setEnabled(false);
            CityBox.setEnabled(false);
            StateBox.setEnabled(false);
            SetAddressButton.setEnabled(false);
            jLabel7.setText(address + " " + city + ", " + state);
            DepositBox.setEnabled(true);
            MonthsBox.setEnabled(true);
            RateBox.setEnabled(true);
            SetBankInfoButton.setEnabled(true);
        }
        
    }//GEN-LAST:event_SetAddressButtonActionPerformed

