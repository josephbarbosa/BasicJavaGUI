    private void SetBankInfoButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_SetBankInfoButtonActionPerformed
        String principalString = "";
        String monthString = "";
        String rateString = "";
        
        double principal;
        int months;
        double rate;
        
        try {
            principalString = DepositBox.getText();
            monthString = MonthsBox.getText();
            rateString = RateBox.getText();
            
            principal = Double.parseDouble(principalString);
            months = Integer.parseInt(monthString);
            rate = (Double.parseDouble(rateString));
            String principalOutput = "";
            principalOutput = principalOutput.format("%.2f", principal); //AFTER PARSING EXCEPTION CHECK
            String monthOutput = Integer.toString(months);
            String rateOutput = Double.toString(rate);
            
            object.setUserPrincipal(principal);
            object.setUserMonths(months);
            object.setUserAnnualRate(rate);
            jLabel11.setText("Deposit: $" + principalOutput + " || Months: " + monthOutput + " || Rate: " + rateOutput + "%");
            SetBankInfoButton.setEnabled(false);
            CalculateButton.setVisible(true);
            jProgressBar1.setValue(80);
            DepositBox.setEnabled(false);
            MonthsBox.setEnabled(false);
            RateBox.setEnabled(false);
            DepositBox.setText("");
            MonthsBox.setText("");
            RateBox.setText("");
            
        }
        catch (NumberFormatException e)
        {
            jLabel11.setText("ERROR! Data NOT SET! Complete ALL fields with NUMERICAL values ONLY.");
            DepositBox.setText("");
            MonthsBox.setText("");
            RateBox.setText("");
        }
        
       
        
        
    }//GEN-LAST:event_SetBankInfoButtonActionPerformed

